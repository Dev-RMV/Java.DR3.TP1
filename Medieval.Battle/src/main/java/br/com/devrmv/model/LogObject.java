package br.com.devrmv.model;

public class LogObject {

	String date;
	String selectecClass;
	String result;
	String randomMonster;

	public String getDate() {
		return date;
	}

	public void setDate(String date) {
		this.date = date;
	}

	public String getSelectecClass() {
		return selectecClass;
	}

	public void setSelectecClass(String selectecClass) {
		this.selectecClass = selectecClass;
	}

	public String getResult() {
		return result;
	}

	public void setResult(String result) {
		this.result = result;
	}

	public String getRandomMonster() {
		return randomMonster;
	}

	public void setRandomMonster(String randomMonster) {
		this.randomMonster = randomMonster;
	}

	public int getRounds() {
		return rounds;
	}

	public void setRounds(int rounds) {
		this.rounds = rounds;
	}

	int rounds;

}
